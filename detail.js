function escapeHtml(value) {
  return String(value)
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#39;');
}

function classifySourceParagraph(paragraph, index, pageNumber = 1) {
  if (index === 0 && pageNumber === 1) return 'source-title';
  if (/^(Chỉnh sửa|Đánh giá|Kết quả|Đầu ra|Hiệu quả|Prompt)\b/i.test(paragraph)) return 'source-callout';
  if (/^[0-9]+[.)]\s/.test(paragraph) || paragraph.includes('->')) return 'source-step';
  if (/^(PHẦN|I\.|II\.|III\.|IV\.|V\.|VI\.)\s/.test(paragraph)) return 'source-heading';
  if (/^(Bước|Prompt|Kết quả|Chỉnh sửa|Đánh giá|Mục tiêu|Nội dung|Tình huống|Vai trò|Họ và tên|MSV|Lớp|Môn học|Tên đề tài|Các công cụ)/i.test(paragraph)) return 'source-subheading';
  return 'source-body';
}

function renderTable(table) {
  if (!table) return '';
  return `
    <div class="summary-table-wrap">
      <table class="summary-table">
        <thead>
          <tr>${table.headers.map((header) => `<th>${header}</th>`).join('')}</tr>
        </thead>
        <tbody>
          ${table.rows.map((row) => `
            <tr>${row.map((cell) => `<td>${cell}</td>`).join('')}</tr>
          `).join('')}
        </tbody>
      </table>
    </div>
  `;
}

function renderSourceTables(tables) {
  if (!tables?.length) return '';
  return tables.map((rows, index) => {
    const [header = [], ...bodyRows] = rows;
    return `
      <div class="source-table-block">
        <p class="source-table-label">Bảng ${index + 1}</p>
        <div class="summary-table-wrap">
          <table class="summary-table">
            <thead>
              <tr>${header.map((cell) => `<th>${escapeHtml(cell)}</th>`).join('')}</tr>
            </thead>
            <tbody>
              ${bodyRows.map((row) => `
                <tr>${row.map((cell) => `<td>${escapeHtml(cell)}</td>`).join('')}</tr>
              `).join('')}
            </tbody>
          </table>
        </div>
      </div>
    `;
  }).join('');
}

