function numberSectionImages(sections) {
  let imageNumber = 0;
  return sections.map((section) => ({
    ...section,
    images: (section.images || []).map((image) => {
      if (Array.isArray(image)) {
        const [src, caption, variant] = image;
        return {
          src,
          caption,
          page: section.page,
          number: imageNumber += 1,
          variant: variant || 'wide'
        };
      }

      return {
        ...image,
        page: image.page || section.page,
        number: image.number || (imageNumber += 1),
        variant: image.variant || 'wide'
      };
    })
  }));
}

